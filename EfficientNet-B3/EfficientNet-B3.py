print("✅ Đã lưu model (.h5)   : 'weather_efficientnetb3_finetuned.h5'")

print("\n   Load lại .keras : tf.keras.models.load_model('weather_efficientnetb3_finetuned.keras')")
print("   Load lại .h5    : tf.keras.models.load_model('weather_efficientnetb3_finetuned.h5')")